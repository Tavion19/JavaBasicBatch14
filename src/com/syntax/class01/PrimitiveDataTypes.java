package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//storing and representing whole number value
	byte box1 = 20; 

	short box2=32767;
		
	int box3=100000; // popular
	
	long box4=100000000l; //credit cards, transactions
	
	
	float variable1 = 1.99F;
	
	double variable2 = 99.0; //popular largest primitive data type
	
	//storing single characters
	
	char container='A';
	char dollar='$';
	
	//storing yes or no values
	boolean hot=true;
	boolean tired=false;	
	
	System.out.println(variable2);//printing value of the variable
	System.out.println("variable2"); //printing text variable
	
	System.out.println(tired); //must be lowercase as variable 'tired' above
	
	//System.out.println(variable3); error - variable does not exist.
	
	
	
	System.out.println(box1);
	System.out.println(box2);
	System.out.println(box3);
	System.out.println(box4);
	System.out.println(variable1);
	System.out.println(variable2);
	System.out.println(container);
	System.out.println(dollar);
	System.out.println(hot);
	System.out.println(tired);
	
	}
	

}
